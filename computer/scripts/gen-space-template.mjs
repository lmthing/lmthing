#!/usr/bin/env node
/**
 * Reads space directories and outputs a FileSystemTree-compatible JS object
 * suitable for embedding in template.ts.
 *
 * Usage:
 *   node scripts/gen-space-template.mjs <dir1> [dir2] [--name <key>]
 *
 * Example:
 *   node scripts/gen-space-template.mjs \
 *     ../sdk/org/cli/spaces/codebase \
 *     ../sdk/libs/spaces/knowledge
 */

import { readFileSync, readdirSync, statSync } from 'fs'
import { join, relative } from 'path'

function buildTree(dir) {
  const tree = {}
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry)
    const stat = statSync(full)
    if (stat.isDirectory()) {
      tree[entry] = { directory: buildTree(full) }
    } else {
      const contents = readFileSync(full, 'utf8')
      tree[entry] = { file: { contents } }
    }
  }
  return tree
}

const dirs = process.argv.slice(2)
if (!dirs.length) {
  console.error('Usage: node gen-space-template.mjs <dir1> [dir2 ...]')
  process.exit(1)
}

const result = {}
for (const dir of dirs) {
  // Use the last path segment as the key
  const key = dir.replace(/\/$/, '').split('/').pop()
  result[key] = { directory: buildTree(dir) }
}

const json = JSON.stringify(result, null, 2)
console.log(`// Auto-generated by scripts/gen-space-template.mjs — do not edit manually\n// Run: pnpm gen-spaces\nimport type { FileSystemTree } from './template'\nexport const spacesSnapshot: FileSystemTree = ${json} as unknown as FileSystemTree\n`)
